package TD1;

public class Ex5 {
    public static void run() {
        System.out.println("-- Exercice 5 --");
        int aInt = 7;
        int bInt = 2;
        System.out.println("int a = 7; int b = 2; a / b = " + (aInt / bInt));
        double aDouble = 7.0;
        double bDouble = 2.0;
        System.out.println("double a = 7.0; double b = 2.0; a / b = " + (aDouble / bDouble));
        System.out.println("Explication : int/int donne une division entière, double/double donne un résultat décimal\n");
    }
}

